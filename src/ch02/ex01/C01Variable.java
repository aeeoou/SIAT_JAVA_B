package ch02.ex01;

public class C01Variable {
	public static void main(String[] args) {
		boolean bl = false;
		bl = true;
		
		byte b = 0;   // 1바이트
		short s = 0;  // 2바이트
		char c = 0;   // 2바이트
		int i = 0;    // 4바이트
		long l = 0;   // 8바이트
		float f = 0.0f; // 4바이트
		double d = 0.0; // 8바이트
		
		String str = "hello";
		
		// 변수 선언 방법1
		int a;
		a = 0;  // initialization
		
		// 변수 선언 방법2
		int x;
		int y;
		int x2, y2;
		int x3 = 0, y3 = 0;
		
	}
}
